// What is Queue?
// Queue is a linear data structure that follows the
// First In First Out (FIFO) principle.

// In a queue:
// - Insertion (enqueue) is done at the REAR.
// - Deletion (dequeue) is done from the FRONT.


// Basic Queue Operations:
// enqueue -> Used to insert an element at the REAR of the queue.
// dequeue -> Used to remove (delete) an element from the FRONT of the queue.
// front / peek -> Returns the FRONT element of the queue without removing it.


// Queue Overflow & Underflow:
// Queue Overflow:
// Occurs when we try to ENQUEUE (add) an element
// into a queue that is already FULL.

// Queue Underflow:
// Occurs when we try to DEQUEUE (remove) an element
// from an EMPTY queue.


// Time Complexity of Queue Operations:

// Enqueue → O(1)
// Dequeue → O(1)
// Peek    → O(1)


// Real-life Examples of Queue:
// - Line at a ticket counter
// - CPU task scheduling
// - Printer queue
