// What is Stack?
// Stack is a linear data structure that follows the Last In First Out (LIFO) principle, where all operations are performed from one end called the top.


// Basic Stack Operation : push , pop , peek
// push → Used to add an element to the TOP of the stack.
// pop  → Used to remove (delete) the element from the TOP of the stack.
// peek → Used to view the element at the TOP of the stack without removing it.


// Stack Overflow and Underflow
// Stack Overflow -> when we try to push(add) element in an stack which is full then it give error as stack overflow
// Stack Underflow -> when we try to pop(delete) element form an empty stack then it give error as stack underflow

// Time Complexity of Stack Operation
// Push  → O(1)
// Pop   → O(1)
// Peek  → O(1)

// real-life examples of stack
// Stack is used wherever the most recent action needs to be accessed first, such as undo operations, function calls, and browser navigation.

